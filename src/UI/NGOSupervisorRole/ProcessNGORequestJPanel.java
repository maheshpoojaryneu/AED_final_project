    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        
        
        String message = resultJTextField.getText();
        
        
         if(message==null||message.equals("")){
       JOptionPane.showMessageDialog(null, "Enter Request Message");
       return;
       }
        
        DoctorWorkRequest doctorRequest = new DoctorWorkRequest();
        
        doctorRequest.setMessage(message);
        doctorRequest.setId(idTxt.getText());
        doctorRequest.setSender(userAccount);
        doctorRequest.setTestResult(resultJTextField.getText());
        doctorRequest.setStatus("Sent to Doctor");
        doctorRequest.setName1(nameTxt.getText());
        doctorRequest.setAge(Integer.parseInt(ageTxt.getText()));
        doctorRequest.setLocation(locationTxt.getText());
        doctorRequest.setGender(gendertxt.getText());
        doctorRequest.setCloth(request.getCloth());
        doctorRequest.setFood(request.getFood());
        doctorRequest.setTreatment(request.getTreatment());
        doctorRequest.setCriminal(request.getCriminal());
        doctorRequest.setFoodCost(request.getFoodCost());
        doctorRequest.setClothCost(request.getClothCost());
        
        
        
        
        
Organization org = null;
        for(Network network : business.getNetworkList()){
           // if (enterprise instanceof HospitalEnterprise){
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
           
       
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof DoctorOrganization){
                org = organization;
                break;
            }
        }
        
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(doctorRequest);
            userAccount.getWorkQueue().getWorkRequestList().add(doctorRequest);
            break;
            //System.out.println(doctorRequest);
        }
        }
        //}
        }  
        
        request.setStatus("Sent to Doctor");
        
        
           try {
            Email.sendMail("studentatnortheastern@gmailcom","Treatment Required for "+request.getName1(),"Information of "+request.getName1()+"has been added to the queue for Treatment . Please take necessary action");
            
        } catch (Exception ex) {
            Logger.getLogger(PoliceViewRecordJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
           
          JOptionPane.showMessageDialog(null, "Information Sent to Doctor");  
          
          
          Logger logger = Logger.getLogger("MyLog");  
      FileHandler fh;  

    try {  

        // This block configure the logger with handler and formatter  
        fh = new FileHandler("D:/MyLogFile.log");  
        logger.addHandler(fh);
        SimpleFormatter formatter = new SimpleFormatter();  
        fh.setFormatter(formatter);  

        // the following statement is used to log any messages  
        logger.info("Request for "+doctorRequest.getName1()+"has been sent to Doctor by "+userAccount);  

    } catch (SecurityException e) {  
        e.printStackTrace();  
    } catch (IOException e) {  
        e.printStackTrace();  
    } 
    }//GEN-LAST:event_submitJButtonActionPerformed

