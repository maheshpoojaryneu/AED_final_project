    private void showResult()
    {
        idTxt.setText(request.getId());
        nameTxt.setText(request.getName1());
        ageTxt.setText(String.valueOf(request.getAge()));
        locationTxt.setText(request.getLocation());
        gendertxt.setText(request.getGender());
        
        
        if(request.getCloth())
        clothTxt.setText("No");
        else
              clothTxt.setText("Provided");

         if(request.getFood())
        foodTxt.setText("No");
        else
              foodTxt.setText("Provided");
         
        if(request.getTreatment())
        {
        treatmentTxt.setText("Yes");
        
        submitJButton.setEnabled(true);
        btnBilling.setEnabled(false);
        
        }
        else
        {
              treatmentTxt.setText("No");
              
              
              submitJButton.setEnabled(false);
        btnBilling.setEnabled(true);
        
        }
        if(request.getCriminal())
        criminalTxt.setText("Yes");
        else
              criminalTxt.setText("No");
        
        
        
        
        
    }
